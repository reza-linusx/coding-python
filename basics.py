# Python Data Types Examples

# Integer: whole numbers
integ = 44

# Float: numbers with decimals
flt = 3.14

# String: text enclosed in quotes
my_str = 'this is my string'

# List: ordered collection of items, can be mixed types, mutable
lis = ['pizza', 3.22, 60, False, True]

# Dictionary: key-value pairs, unordered, mutable
dic = {
    'name': 'reza',
    'pi': 3.14,
    'age': 26
}

# Tuple: ordered collection, immutable (cannot be changed after creation)
tup = ("something", True, 33)

# Boolean: True or False values
correct = True
incorrect = False
